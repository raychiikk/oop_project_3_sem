// Клас City представляє географічне місце з назвою та країною.
// Використовує інкапсуляцію для захисту даних про місто.
export class City {
    // Приватне поле для назви міста.
    #name;
    // Приватне поле для країни, до якої належить місто.
    #country;

    // Конструктор ініціалізує об'єкт із назви міста та країни.
    constructor(name, country) {
    this.#name = name;
    this.#country = country;
    }

    // Getter для отримання назви міста (тривіальний метод).
    getName() { return this.#name; }

    // Нетривіальний метод, який повертає деталі локації у форматі рядка.
    // Демонструє обробку приватних полів.
    getLocationDetails() { return `${this.#name}, ${this.#country}`; }

    // Нетривіальний метод, який імітує пошук сусідніх міст (поки повертає фіксований список).
    findNearby() { return ['Lviv', 'Odessa']; }
}