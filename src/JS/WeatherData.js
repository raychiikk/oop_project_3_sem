// Клас WeatherData представляє дані про поточну погоду.
// Використовує інкапсуляцію для приховання внутрішніх даних (температура та опис).
// Може бути частиною ієрархії або використовуватися як базовий об'єкт для прогнозу.
export class WeatherData {
    // Приватне поле для температури (у °C), доступне тільки всередині класу.
    #temperature;
    // Приватне поле для текстового опису погоди (наприклад, "Sunny" або "Rainy").
    #description;

    // Конструктор ініціалізує об'єкт із заданими значеннями температури та опису.
    constructor(temp, desc) {
    this.#temperature = temp;
    this.#description = desc;
    }

    // Getter для отримання значення температури (тривіальний метод).
    getTemperature() { return this.#temperature; }

    // Setter для оновлення значення температури (тривіальний метод).
    setTemperature(temp) { this.#temperature = temp; }

    // Нетривіальний метод, який повертає зведену інформацію про погоду.
    // Демонструє роботу з приватними полями та формуванням рядка.
    getWeatherSummary() { return `${this.#temperature}°C, ${this.#description}`; }
    // Нетривіальний метод, який перевіряє, чи температура є екстремальною (> 30°C).
    // Використовується для аналізу погодних умов.
    checkExtreme() { return this.#temperature > 30; }
}